local function getFrame(x)

  if (x >= 1 and x <= #frames) then
    return frames[x]
  else
    return nil
  end
end

doggo = {}

function doggo.startDance()

  local frameNum = 1

  return function()
  
    frame = getFrame((frameNum % #frames) + 1)

    frameNum = frameNum + 1
    return frame
  end
end

